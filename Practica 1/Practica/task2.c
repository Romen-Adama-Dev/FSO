#include <stdio.h>

int main() {
    // Declaration of variables
    int numero;
    
    // Input data
    printf("Ingrese un numero: ");
    // Scanf reads a line from stdin and stores it in the variable
    scanf("%d", &numero);
    
    // Output data
    printf("El numero en base hexadecimal es: %X\n", numero);
    
    return 0;
}

